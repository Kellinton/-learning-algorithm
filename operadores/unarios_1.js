// opera em cima de um único operando
// a++ ou ++a,  é uma forma de atribuição unária, incremento


//---------------------------------------
let a = 1;
a = a + 1; //infix operador no meio dos dois operando
a += 1;
a++; //postix operador depois do operando 
++a; //prefix operador antes do operando 
a--;
--a;

console.log(a);

//---------------------------------------

let b = 10;
b++; // incrementa de 1 em 1

console.log(b);


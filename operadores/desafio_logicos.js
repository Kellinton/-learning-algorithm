// Duas expressões TRUE (Aritméticos/Relacionais/Lógicos)
let resultadoTRUE1 = (7 + 3) === 10 && 4 * 2 >= 2;
let resultadoTRUE2 = 4 - 2 !== 2 || 2 + 1 === 3; 

console.log(resultadoTRUE1); 
console.log(resultadoTRUE2); 

// Duas expressões FALSE (Aritméticos/Relacionais/Lógicos)

let resultadoFALSE1 = ((4 + 2) - 2) !== 4 && (4 + 1) > 4; 
let resultadoFALSE2 = !(5 + 2 === 7 && 4 + 2 === 6);

console.log(resultadoFALSE1);
console.log(resultadoFALSE2);
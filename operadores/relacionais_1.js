// o resultado será false ou true

// com valores literais

console.log(7 > 9);    //false
console.log(7 >= 7);   //true
console.log(8 < 2);    //false
console.log(8 <= 12);  //true
console.log(9 != 5);   //true


// com variáveis

let a = 7;
let b = 32;

console.log(a > b);    //false
console.log(a >= b);   //false
console.log(a < b);    //true
console.log(a <= b);  //true
console.log(a != b);   //true
